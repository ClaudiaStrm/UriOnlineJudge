'''
Leia quatro valores inteiros A, B, C e D. A seguir, calcule e mostre a diferença do produto de A e B pelo produto de C e D segundo a fórmula: DIFERENCA = (A * B - C * D).
'''

# -*- coding: utf-8 -*-
A = int(input())
B = int(input())
C = int(input())
D = int(input())

DIFERENCA = (A * B) - (C * D)

print('DIFERENCA = %d' %DIFERENCA)